void System::GlobalBA(size_t iterations)
{
    bool mapping_running = !mpLocalMapper->isFinished();
    bool viewer_running = (mpViewer && !mpViewer->isFinished());
    
    if (mapping_running)
    {
        mpLocalMapper->RequestStop();
        while(!mpLocalMapper->isStopped() || mpLoopCloser->isRunningGBA())
        {
            usleep(5000);
        }

    }
    if (mpViewer && viewer_running)
    {
        mpViewer->RequestStop();
        while(!mpViewer->isStopped())
        {
            usleep(5000);
        }
    }
    
    mpLoopCloser->RunGlobalBundleAdjustment(mpTracker->mCurrentFrame.mpReferenceKF->mnId,iterations);
    if (mapping_running)
        mpLocalMapper->Release();
    if (mpViewer && viewer_running)
        mpViewer->Release();
    
    
  
    
    
}


void System::SaveTrajectoryEx(const string &filename)
{
    cout << endl << "Saving camera trajectory to " << filename << " ..." << endl;
    if(mSensor==MONOCULAR)
    {
        cerr << "ERROR: SaveTrajectoryEx cannot be used for monocular." << endl;
        return;
    }

    vector<KeyFrame*> vpKFs = mpMap->GetAllKeyFrames();
    sort(vpKFs.begin(),vpKFs.end(),KeyFrame::lId);

    // Transform all keyframes so that the first keyframe is at the origin.
    // After a loop closure the first keyframe might not be at the origin.
    cv::Mat Two = vpKFs[0]->GetPoseInverse();


    ofstream ftraject;
    ofstream fids;

    ftraject.open(filename.c_str());
    fids.open("KittiIds.cxt");
    ftraject << fixed;

    // Frame pose is stored relative to its reference keyframe (which is optimized by BA and pose graph).
    // We need to get first the keyframe pose and then concatenate the relative transformation.
    // Frames not localized (tracking failure) are not saved.

    // For each frame we have a reference keyframe (lRit), the timestamp (lT) and a flag
    // which is true when tracking failed (lbL).
    list<ORB_SLAM2::KeyFrame*>::iterator lRit = mpTracker->mlpReferences.begin();
    list<double>::iterator lT = mpTracker->mlFrameTimes.begin();

      for(list<cv::Mat>::iterator lit=mpTracker->mlRelativeFramePoses.begin(), lend=mpTracker->mlRelativeFramePoses.end();lit!=lend;lit++, lRit++, lT++)
    {
        ORB_SLAM2::KeyFrame* pKF = *lRit;

        cv::Mat Trw = cv::Mat::eye(4,4,CV_32F);

        while(pKF->isBad())
        {
            cout << "bad parent" << endl;
            Trw = Trw*pKF->mTcp;
            pKF = pKF->GetParent();
        }

        Trw = Trw*pKF->GetPose()*Two;

        cv::Mat Tcw = (*lit)*Trw;
        cv::Mat Rwc = Tcw.rowRange(0,3).colRange(0,3).t();
        cv::Mat twc = -Rwc*Tcw.rowRange(0,3).col(3);

        ftraject << setprecision(9) << Rwc.at<float>(0,0) << " " << Rwc.at<float>(0,1)  << " " << Rwc.at<float>(0,2) << " "  << twc.at<float>(0) << " " <<
             Rwc.at<float>(1,0) << " " << Rwc.at<float>(1,1)  << " " << Rwc.at<float>(1,2) << " "  << twc.at<float>(1) << " " <<
             Rwc.at<float>(2,0) << " " << Rwc.at<float>(2,1)  << " " << Rwc.at<float>(2,2) << " "  << twc.at<float>(2) << endl;
        
    }


    for (auto kfptr : vpKFs)
    {
        fids << kfptr->mnFrameId << endl;
    }

    ftraject.close();
    fids.close();
    cout << endl << "trajectory saved!" << endl;
}


void System::SaveBow() 
{
    vector<ORB_SLAM2::KeyFrame*> vpKFs = mpMap->GetAllKeyFrames();
    sort(vpKFs.begin(),vpKFs.end(),KeyFrame::lId);
    cout << "Vocab Size" << mpVocabulary->size() << endl;
    for (auto pKF : vpKFs)
    {
        stringstream ofilename;
        ofilename << "BoW" << pKF->mnId << ".m";
        cout << ofilename.str() << endl;
        pKF->mBowVec.saveM(ofilename.str(),mpVocabulary->size());
    }
}



